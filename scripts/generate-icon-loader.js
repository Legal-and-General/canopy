#!/usr/bin/env node

/**
 * Generates a static icon loader that uses lazy chunks.
 * This approach works with the new Angular builder by avoiding dynamic import template literals.
 * Instead, it creates a static mapping of icon names to lazy import functions.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

function generateIconLoader(config) {
  const {
    sourceDir,
    outputFile,
    prefix,
    interfaceName,
    interfaceFile,
    loaderConstName = 'ICON_LOADER',
  } = config;

  // Read all icon files
  const iconsDir = path.join(sourceDir, 'set');
  const files = fs.readdirSync(iconsDir)
    .filter(file => file.endsWith('.icon.ts') && !file.includes('Array'));

  // Determine the interface file name
  const interfaceFileMap = {
    'lgIcon': 'ui-icons-files',
    'lgPictogram': 'pictogram-files',
    'lgFlagIcon': 'flag-icons-files',
  };
  const resolvedInterfaceFile =
    interfaceFile ?? interfaceFileMap[prefix] ?? `${path.basename(sourceDir)}`;

  const formatPropertyKey = iconKey => (/^[A-Za-z_$][A-Za-z0-9_$]*$/.test(iconKey)
    ? iconKey
    : `'${iconKey}'`);

  // Generate the lazy loader entries
  const loaderEntries = files
    .map(file => {
      const iconFileName = file.replace('.icon.ts', '');
      const iconKey = iconFileName.replace(`${prefix}-`, '');
      const iconFilePath = path.join(iconsDir, file);
      const iconFileContent = fs.readFileSync(iconFilePath, 'utf-8');
      const exportMatch = iconFileContent.match(/export const\s+([A-Za-z0-9_]+)\s*:/);

      if (!exportMatch) {
        throw new Error(`Could not determine export name for ${iconFilePath}`);
      }

      const exportName = exportMatch[1];

      return `  ${formatPropertyKey(iconKey)}: () => import('../${path.basename(sourceDir)}/set/${iconFileName}.icon').then(m => m.${exportName}),`;
    })
    .join('\n');

  // Generate the file content
  const content = `/* 🤖 This file is auto-generated by scripts/generate-icon-loader.js */
import type { ${interfaceName} } from '../${path.basename(sourceDir)}/set/${resolvedInterfaceFile}.interface';

/**
 * Lazy icon loader using static imports.
 * Each icon is loaded on-demand using dynamic import(), but the imports are statically defined
 * so they work with the new Angular builder (esbuild).
 *
 * This creates separate lazy chunks for each icon, keeping the initial bundle small.
 */
export const ${loaderConstName}: Record<string, () => Promise<${interfaceName}>> = {
${loaderEntries}
};
`;

  // Write the file
  const outputPath = path.join(__dirname, '..', outputFile);
  fs.writeFileSync(outputPath, content, 'utf-8');
  console.log(`✅ Generated ${outputFile} with ${files.length} lazy icon loaders`);
}

// Generate for all icon sets
generateIconLoader({
  sourceDir: 'projects/canopy/src/lib/ui-icons-files',
  outputFile: 'projects/canopy/src/lib/icon/icon-loader.ts',
  prefix: 'lgIcon',
  interfaceName: 'Icon',
});

generateIconLoader({
  sourceDir: 'projects/canopy/src/lib/pictogram-files',
  outputFile: 'projects/canopy/src/lib/pictogram/pictogram-loader.ts',
  prefix: 'lgPictogram',
  interfaceName: 'Pictogram',
  interfaceFile: 'pictogram-files',
  loaderConstName: 'PICTOGRAM_LOADER',
});

generateIconLoader({
  sourceDir: 'projects/canopy/src/lib/flag-icons-files',
  outputFile: 'projects/canopy/src/lib/flag-icon/flag-icon-loader.ts',
  prefix: 'lgFlagIcon',
  interfaceName: 'FlagIcon',
});

console.log('\n🎯 Icon loader files generated successfully!');
console.log('   Each icon will be loaded as a separate lazy chunk on-demand.\n');
